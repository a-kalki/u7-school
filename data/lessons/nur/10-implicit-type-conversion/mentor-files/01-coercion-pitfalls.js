// ? Слева направо: '1'+2 = '12' (конкатенация), потом '12'+3 = '123'

console.log('1 + "2" + 3 =', 1 + '2' + 3);
// ? А здесь? Результат: '123'

console.log('1 + 2 - "1" =', 1 + 2 - '1');
// ? Сначала 1+2=3 (числа), потом 3-'1'=2 (строка приводится к числу)
// ? Почему - не делает конкатенацию? (у минуса нет такой задачи)

