console.log('=== Блок 3: falsy — не так очевидно ===');

// 6 falsy-значений: false, 0, '', null, undefined, NaN

console.log('!"" =', !'');
console.log('!0 =', !0);
console.log('!null =', !null);
console.log('!undefined =', !undefined);
console.log('!NaN =', !NaN);
console.log('!false =', !false);

// А теперь неочевидные:
console.log('!"0" =', !'0');
// ? '0' — строка, не пустая. А 0 — число. Это разные вещи!

console.log('![] =', ![]);
// ? Пустой массив — объект, а объекты всегда truthy

console.log('!{} =', !{});
// ? Пустой объект — тоже truthy

console.log('!"false" =', !'false');
// ? Строка 'false' — непустая, поэтому truthy. Не путай с false!
