console.log('=== Блок 2: -, *, / — всегда числа ===');

let a = '10';
let b = '5';
console.log('"10" - "5" =', a - b);
// ? Минус не умеет конкатенацию — всегда приводит к числу. Результат 5

console.log('"10" * "5" =', a * b);
console.log('"10" / "5" =', a / b);

// А если строка не число?
let c = 'abc';
console.log('"abc" * 1 =', c * 1);
// ? Не число → NaN. Важно проверять ввод пользователя

console.log('');

