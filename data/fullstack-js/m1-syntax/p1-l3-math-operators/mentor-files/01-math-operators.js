console.log('=== Блок 2: Секреты остатка от деления ===');

const n = 9;
console.log('n % 2:', n % 2); // если остаток 0, значит n четное
console.log('n % 3:', n % 3); // если остаток 0, значит n кратное 3
console.log('2 % 3:', 2 % 3); // делитель больше делимого -> делимое
console.log(`2.15 % 1: ${2.15 % 1}`); // остаток от деления на 1 -> дробная часть
console.log(`23 % 10: ${23 % 10}`); // остаток от деления на 10 -> последняя цифра делимого
console.log(`235 % 100: ${235 % 100}`); // две последние цифры делимого
console.log(`23.5 % 10: ${23.5 % 10}`); // последняя с дробной частью

