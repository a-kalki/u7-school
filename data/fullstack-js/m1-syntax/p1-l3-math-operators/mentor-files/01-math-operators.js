console.log('=== Блок 1: Последовательные операции ===');
let x = 10;
console.log('x =', x);

x = x + 5;
console.log('x = x + 5  →', x);

x = x * 3;
console.log('x = x * 3  →', x);

x = x % 4;
console.log('x = x % 4  →', x);

x = (x + 1) ** 4;
console.log('x = (x + 1) ** 4  →', x);

console.log('');

